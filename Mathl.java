import java.lang.Math;
public class Mathl {
    public static void main(String[] args) {
        double x;
        x = Math.max(22,33);
        System.out.println(x);
        x = Math.sqrt(64);
        System.out.println(x);
        x = Math.abs(-55);
        System.out.println(x);
        x = Math.pow(16, 3);
        System.out.println(x);
        x = Math.min(22,33);
        System.out.println(x);
        x = Math.log(3);
    }
    
    
}
